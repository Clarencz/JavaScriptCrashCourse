//= assignment operator
//== comparison operator ( compare if values are equal )
//=== strict equality operator(compare if values $ datatype are equal)
//!= inequality operator
// !== strict inequality operator

const PI = 3.14;
if (PI == 3.14) {
  console.log("THIS IS PI");
} else {
  console.log("not PI");
}
if (PI !== "3.14") {
  console.log("THIS IS NOT PI");
} else {
  console.log("THIS IS  PI");
}
if (PI === "3.14") {
  console.log("THIS IS PI");
} else {
  console.log("not PI");
}
if (PI === 3.14) {
  console.log("THIS IS PI");
} else {
  console.log("not PI");
}
